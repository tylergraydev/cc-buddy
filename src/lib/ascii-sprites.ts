export const SPECIES_LIST = Object.keys(BODIES);

export function getAsciiFrame(
  state: BuddyState,
  tick: number,
  species: string = "capybara",
): string[] {
  const frames = BODIES[species] ?? BODIES.capybara;
  // Use all 3 frames for idle/talking, alternate first 2 for walk
  if (state === "walking") {
    return frames[tick % 2] ?? frames[0];
  }
  return frames[tick % frames.length];
}
