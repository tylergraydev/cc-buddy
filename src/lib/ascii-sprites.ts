const HAT_LINES: Record<string, string> = {
  none: "",
  crown: "   \\^^^/    ",
  tophat: "   [___]    ",
  propeller: "    -+-     ",
  halo: "   (   )    ",
  wizard: "    /^\\     ",
  beanie: "   (___)    ",
  tinyduck: "    ,>      ",
};

export const SPECIES_LIST = Object.keys(BODIES);

export function getAsciiFrame(
  state: BuddyState,
  tick: number,
  species: string = "capybara",
  eye: string = "·",
  hat: string = "none",
): string[] {
  const frames = BODIES[species] ?? BODIES.capybara;
  const frameIdx = state === "walking" ? tick % 2 : tick % frames.length;
  const frame = (frames[frameIdx] ?? frames[0]).map((line) =>
    line.replaceAll("{E}", eye),
  );

  // Hat goes on first line; if first line has content (like ear wiggle), prepend hat above it
  if (hat !== "none" && HAT_LINES[hat]) {
    if (frame[0].trim()) {
      frame.unshift(HAT_LINES[hat]);
    } else {
      frame[0] = HAT_LINES[hat];
    }
  }

  return frame;
}
